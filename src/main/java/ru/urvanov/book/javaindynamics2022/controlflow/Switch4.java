package ru.urvanov.book.javaindynamics2022.controlflow;

public class Switch4 {
    public static void main(String[] args) {
        int mode = 1;

        switch (mode) {
            case 0:
                System.out.println("mode 0");
                break;
            case 1:
                System.out.println("mode 1");
                break;
            case 2:
                System.out.println("mode 2");
                break;
        }
    }
}
